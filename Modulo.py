def s(m):
    print('----------------------------------------------------------------------------------------------')
    S = m + 1 + 2
    print(f"{m} + 1 + 2 =", S)   
    if m >= 1:
        print(f"{m} - 1 =", m - 1)
    else:
        print(f"1 - {m} =", 1 - m)
    P = m * 1 * 2
    print(f"{m} x 1 x 2 =", P)
    print (f"{P} : {S} =", P / S)
    print('----------------------------------------------------------------------------------------------')
def t(n):
    print(f"{n} + 3 + 2 + 1 =", n + 3 + 2 + 1)
    print(f"{n} - 3 - 2 - 1 =", n - 3 - 2 - 1)
    print(f"{n} x 3 x 2 x 1 =", n * 3 * 2 * 1)
    print(f"{n} : 3 : 2 : 1 =", n / 3 / 2 / 1)
    print('----------------------------------------------------------------------------------------------')
